package EjerciciosT2Basicos;

public class Ejercicio17 {
    public static void main(String[] args) {
        // Escribir un programa en Java que imprima 200 veces la palabra “hola”.
        // Nota: en el código fuente que usted escriba debe figurar solamente una vez la palabra “hola”.

        String saludo = "hola";

        for (int i = 1; i <= 200; i++) {
            System.out.println(saludo);
        }
    }
}
